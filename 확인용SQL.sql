SELECT EMP_NAME, JOB_NAME, SALARY, SALARY * 12
FROM EMPLOYEE
JOIN JOB USING (JOB_CODE)
WHERE EMP_NAME IN (SELECT EMP_NAME
							FROM EMPLOYEE
							WHERE JOB_NAME = '부사장'
							AND SALARY > 2000000);

						

